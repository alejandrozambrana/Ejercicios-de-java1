/**
 * Ejercicio 2
 * Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle while .
 * 
 * @author Alejandro Zambrana Naranjo
 */

public class Ejercicio2 {
  public static void main(String[] args) {
    
    int i = 0;
    
    while (i < 101) {
    System.out.println(i);
    i = i + 5;
    }
  }
}


